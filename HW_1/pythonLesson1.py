# Homework 1
# Task 1:
# "Створіть дві змінні first=10, second=30.
# Виведіть на екран результат математичної взаємодії (+, -, *, / и тд.) для цих чисел"

first = 10
second = 30

result_addition = first + second
print(result_addition)

result_subtraction = first - second
print(result_subtraction)

result_multiplication = first * second
print(result_multiplication)

result_division = first / second
print(result_division)

result_int = first // second
print(result_int)

result_rest = first % second
print(result_rest)

result_exponentiation = first ** second
print(result_exponentiation)

